package model;

public class Fact {

    private String factText;

    // Constructor
    public Fact(String factText) {
        this.factText = factText;
    }

    // Getter
    public String getFactText() {
        return factText;
    }

    // Setter
    public void setFactText(String factText) {
        this.factText = factText;
    }
}